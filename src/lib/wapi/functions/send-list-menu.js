/**
 * Send List menu
 * @param {string} to the numberid xxx@c.us
 * @param {string} title the titulo
 * @param {string} description the description
 * @param {string} buttonText the name button
 * @param {array} menu List menu
 */
export async function sendListMenu(to, title, description, buttonText, menu) {
  if (!title && typeof title != 'string') {
    return WAPI.scope(null, true, 404, 'Enter the title variable as an string');
  }

  if (!description && typeof description != 'string') {
    return WAPI.scope(
      null,
      true,
      404,
      'Enter the description variable as an string'
    );
  }

  if (!buttonText && typeof buttonText != 'string') {
    return WAPI.scope(
      null,
      true,
      404,
      'Enter the buttonText variable as an string'
    );
  }

  if (!menu && menu != 'array') {
    return WAPI.scope(null, true, 404, 'Enter the menu variable as an object');
  }

  const chat = await WAPI.sendExist(to);

  if (chat && chat.status != 404 && chat.id) {
    const inChat = await WAPI.getchatId(chat.id).catch(() => {});

    if (inChat) {
      chat.lastReceivedKey._serialized = inChat._serialized;
      chat.lastReceivedKey.id = inChat.id;
    }

    let options = {
      title: title,
      buttonText: buttonText,
      description: description,
      sections: menu
    };

    let result = await WPP.chat.sendListMessage(to, options);

    let newMsgId = WAPI.newId(result);
    let m = { type: 'ListMenu', options };
    if (result.sendMsgResult._value === 'OK') {
      let obj = WAPI.scope(newMsgId, false, result.sendMsgResult._value, null);
      Object.assign(obj, m);
      return obj;
    } else {
      let obj = WAPI.scope(newMsgId, true, result.sendMsgResult._value, null);
      Object.assign(obj, m);
      return obj;
    }
  } else {
    return chat;
  }
}
